import type { ReviewRequest } from '../ai/types';
import type { ReviewIssue } from '../../types/review';
import type { ContractType } from './contractDetector';

/**
 * buildReviewPrompt
 * 构建合同审查的系统 Prompt（专业法律版）
 *
 * 六大审查维度：
 * 1. 风险条款     — 不平等条款、霸王条款、举证倒置
 * 2. 缺失条款     — 合同法要素缺失（违约责任、质量标准、验收规则等）
 * 3. 合规性       — 违反法律强制性规定、违背公序良俗、无效条款
 * 4. 条款分析     — 歧义条款、逻辑矛盾、前后不一致
 * 5. 违约与救济   — 违约责任不对等、违约金标准、损害赔偿上限
 * 6. 争议解决     — 管辖条款、仲裁协议有效性
 */
export function buildReviewPrompt(req: ReviewRequest & {
    contractType?: ContractType | 'custom';
    standpoint?: string | undefined;
    globalInstruction?: string | undefined;
    selectedTemplate?: {
        name: string;
        prompt: string;
    } | undefined;
}): string {
    const depthConfig: Record<
        ReviewRequest['depth'],
        { instruction: string; issueCount: string }
    > = {
        quick: {
            instruction:
                '聚焦最重大的法律风险，快速识别3-5个核心问题，每发现1个问题立即输出，无需等待全部分析完成。',
            issueCount: '3-5个',
        },
        standard: {
            instruction:
                '进行全面的六维度审查，识别10-20个关键问题，每发现1个问题立即输出，同时兼顾效率与深度。',
            issueCount: '10-20个',
        },
        deep: {
            instruction:
                '逐段逐条精细分析，对每个条款分别评估法律效力、公平性和操作可行性。识别所有潜在问题和改进空间，每发现1个问题立即输出。',
            issueCount: '20个以上',
        },
    };

    const cfg = depthConfig[req.depth];

    const typeSpecificFocus = req.selectedTemplate
        ? `\n---\n\n## 专属审查重点（用户自定义模板：${req.selectedTemplate.name}）\n\n${req.selectedTemplate.prompt}`
        : getContractTypeFocus(req.contractType as ContractType);

    return `你是一位具有20年执业经验的中国资深合同法律顾问，曾在一线律师事务所担任合伙人，精通《民法典》合同编、《劳动合同法》、《公司法》、《著作权法》等相关领域法律。

## 核心任务
对提交的合同文本进行系统性、专业性法律审查。${cfg.instruction}

## 审查六大维度

### 1. 风险条款（risk_clause）
识别对委托方不利的条款：
- 权利义务严重不对等（如无限期责任、无限连带担保）
- 单方变更权（甲方单方解除合同、单方修改价格）
- 举证责任倒置（违法将举证责任转移给弱势方）
- 过高违约金或不合理损失赔偿条款
- 免责条款是否违反《民法典》第 506 条强制性规定

### 2. 缺失条款（missing_clause）
检查合同要素是否完整（参照《民法典》第 470 条）：
- 质量标准、技术规格是否明确
- 验收标准、验收周期是否约定
- 付款方式与付款条件是否清晰
- 违约责任（违约金数额、计算方式）是否完备
- 不可抗力认定标准和通知程序
- 合同变更和解除程序

### 3. 合规性（compliance）
审查是否存在以下问题：
- 违反法律、行政法规强制性规定（依《民法典》第 153 条可能无效）
- 违背公序良俗（依《民法典》第 153 条第 2 款）
- 格式条款是否违反《民法典》第 496-498 条（未尽提示义务、显失公平）
- 劳动合同是否符合《劳动合同法》强制性条款（试用期、竞业限制上限等）
- 保密条款是否过度（超出合理范围可能无法执行）

### 4. 条款分析（clause_analysis）
识别条款表述问题：
- 关键术语定义模糊（"合理时间"、"重大违约"等未明确界定）
- 前后条款逻辑矛盾（如第X条与第Y条冲突）
- 数字/日期表述歧义（"工作日"vs"自然日"、起算时间点等）
- 缺乏配套操作程序（如约定了KPI但未约定考核方式）

### 5. 违约与救济
- 违约责任是否具体可执行（金额或计算公式）
- 守约方救济手段是否充分（先行履行抗辩权等）
- 损害赔偿是否合理限额（上限过低可能无法覆盖实际损失）

### 6. 争议解决
- 仲裁协议是否有效（仲裁机构须明确、协议须独立存在）
- 诉讼管辖约定是否合法（不得违反专属管辖）
- 适用法律约定在涉外合同中的有效性

---

## 输出格式（严格遵守，不得偏离）

### 问题条目（每个问题单独一行 JSON，立即输出）：
{"type":"issue","id":"issue-001","category":"risk_clause","riskLevel":"high","title":"违约金条款显失公平","description":"合同第X条约定乙方违约须承担合同总价200%的违约金，远超《民法典》第585条所允许的以弥补损失为目的原则，属于可撤销条款。甲方可利用此条款主张过高违约金，对乙方构成重大不利。","originalText":"<合同中的精确原文，必须是完整可替换单元，禁止截断、禁止省略号>","suggestedText":"<建议修改后的完整文字>","legalBasis":"《民法典》第585条第2款：约定的违约金过分高于造成的损失的，人民法院或者仲裁机构可以根据当事人的请求予以适当减少。"}

### 审查总结（全部问题输出完毕后，单独一行）：
{"type":"summary","content":"<100字以内的整体评估，包括：风险等级（高/中/低）、最主要的2-3个问题、总体建议。注意：总结文字中绝不允许出现「issue-xxx」等内部编号，必须使用纯中文自然语言描述>","model":"${req.model}"}

---

## 关键执行规则

1. **originalText 必须是合同原文逐字引用的【完整可替换单元】**——必须引用到完整的句子或条款段落，确保 suggestedText 替换 originalText 后语句通顺、不会遗留残余文字。例如原文"甲方的付款期限为三年"，不得只引用"甲方的付款期限"而遗漏"为三年"。**对于 missing_clause 也一样**：originalText 应引用缺失条款所在位置附近的已有完整段落/条款原文（作为替换范围），suggestedText 应是在该段落基础上补充缺失条款后的完整替换版本。系统将用 suggestedText 整体替换 originalText，而非在其后插入。**禁止使用“...”或“……”省略原文；禁止截断。长度可超过200字，允许到1200字（以完整性优先）。**
2. **suggestedText 必须是 originalText 的完整替换版本**（所有 category 均适用，包括 missing_clause），直接替换后上下文必须通顺连贯，不得假设保留 originalText 之外的任何文字
3. **legalBasis 必须引用完整法条名称及条款编号**（如《民法典》第470条第8项），禁止笼统引用
4. **riskLevel 量化标准**：
   - high：可能导致合同无效、重大财产损失、严重法律责任
   - medium：存在较大不确定性或执行障碍，建议修改
   - low：表述不够严谨但影响有限，可选择修改
   - info：最佳实践建议，当前表述合法但可优化
5. **禁止在 JSON 之外输出任何文字**（包括分析过程、思考链、Markdown 格式）
6. **所有文本内容使用中文**，特别是 summary 的 content 中**严禁**出现 "issue-001" 等内部编号符号
7. **发现一个问题立即输出一行 JSON**，不要积累后批量输出
${req.standpoint === 'party_a' ? `\n---\n\n## 审查立场\n你以 **甲方法律顾问** 的立场审查此合同。重点识别：\n- 对甲方不利的权利义务分配\n- 甲方潜在的责任风险和经济损失\n- 缺少保护甲方利益的条款\n- 对方可能利用的模糊条款\n在 description 中明确说明该问题对甲方的具体影响。` : req.standpoint === 'party_b' ? `\n---\n\n## 审查立场\n你以 **乙方法律顾问** 的立场审查此合同。重点识别：\n- 对乙方不利的权利义务分配\n- 乙方潜在的责任风险和经济损失\n- 缺少保护乙方利益的条款\n- 甲方可能利用的苛刻条款\n在 description 中明确说明该问题对乙方的具体影响。` : ''}
${typeSpecificFocus}
${req.globalInstruction ? `\n---\n\n## 附加自定义指令\n\n${req.globalInstruction}` : ''}`;
}

/**
 * AI 流式输出行的解析结果类型
 */
export type ParsedLine =
    | { type: 'issue'; data: ReviewIssue }
    | { type: 'summary'; content: string; model: string }
    | { type: 'unknown' };

/**
 * 各合同类型专属审查重点（追加到 prompt 末尾）
 */
function getContractTypeFocus(contractType?: ContractType): string {
    const focuses: Partial<Record<ContractType, string>> = {
        sale: `

---

## 买卖合同专项审查重点
- **标的物质量**：质量标准是否明确（国标/行标/企标/样品），验收标准和异议期是否约定
- **价款与支付**：价款构成（含税/不含税）、支付节点、迟延付款违约金
- **所有权转移**：货物所有权和风险何时转移，与交付方式的衔接
- **数量短缺与瑕疵**：数量不足/质量瑕疵的索赔期限和程序
- **包装与运输**：包装标准、运输费用负担、保险安排`,
        labor: `

---

## 劳动合同专项审查重点
- **试用期合法性**：试用期期限是否符合《劳动合同法》第19条限制；试用期工资不低于80%
- **竞业限制**：补偿金标准（不低于月薪30%）、范围和期限（≤2年）是否合理
- **加班与薪酬**：加班工资计算基数、平时/休息日/法定节假日倍数是否合法
- **社会保险**：五险一金约定，是否存在自愿放弃社保等无效条款
- **解除与经济补偿**：解除条件是否公平，经济补偿金标准是否符合法定要求`,
        lease: `

---

## 租赁合同专项审查重点
- **租赁物现状**：交付时的现状、设施清单、瑕疵告知义务
- **租金与押金**：租金调整机制、押金退还条件和期限
- **维修责任**：日常维修、大修的责任划分
- **转租限制**：是否允许转租，转租须经出租人同意的约定
- **优先续租/优先购买权**：是否赋予承租人优先权
- **非正常损耗**：何为自然损耗，何为赔偿责任`,
        service: `

---

## 服务/委托合同专项审查重点
- **服务范围界定**：服务内容是否清晰可量化，避免"按需提供"等模糊表述
- **验收标准**：服务成果的验收标准和程序，验收期限
- **知识产权归属**：服务过程中产生的成果、工具的知识产权归属
- **保密条款**：适用范围、保密期限、泄露违约责任
- **分包/转委托**：是否允许分包，分包责任承担`,
        loan: `

---

## 借款合同专项审查重点
- **利率合法性**：利率不超过LPR的4倍（年利率≤15.4%），复利约定效力
- **担保有效性**：抵押/质押物的设定程序、登记，保证人资格
- **提前还款**：提前还款权利，是否有违约金
- **逾期利率**：逾期罚息标准，债务加速到期条款
- **资金用途**：借款用途限制，挪用的违约后果`,
        nda: `

---

## 保密协议专项审查重点
- **保密信息范围**：定义是否过宽（应限于特定商业秘密），避免"一切信息均保密"
- **例外情形**：已公知信息、法定披露、自行研发的排除
- **保密期限**：期限是否合理（永久保密条款的执行风险）
- **违约救济**：赔偿金额是否可量化，是否包含申请禁令救济的权利
- **员工约束机制**：被披露方如何确保员工遵守保密义务`,
        construction: `

---

## 建设工程合同专项审查重点
- **资质与许可**：承包人资质、施工许可证、安全生产许可证
- **工程款支付**：进度款节点、结算周期，是否有背靠背付款条款
- **工期与顺延**：工期顺延条件（甲方原因、不可抗力），逾期竣工违约金上限
- **质量保证金**：保证金比例（不超过3%）、缺陷责任期和退还时间
- **设计变更与索赔**：变更程序、索赔通知期限（逾期丧失索赔权的风险）
- **优先受偿权**：建设工程价款优先受偿权的行使期限（竣工/合同约定完工之日起18个月）`,
    };

    return contractType && focuses[contractType] ? focuses[contractType]! : '';
}
